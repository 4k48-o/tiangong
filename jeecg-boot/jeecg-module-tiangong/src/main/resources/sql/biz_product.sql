  `online_end_date` varchar(19) DEFAULT NULL COMMENT '上架结束时间 格式：yyyy-MM-dd HH:mm:ss',
  `product_url` json DEFAULT NULL COMMENT '产品URL列表',
  `wheelchair_access` varchar(50) DEFAULT NULL COMMENT '是否有轮椅通道',
  `skip_line` varchar(50) DEFAULT NULL COMMENT '是否包含免排队',
  `highlights` text DEFAULT NULL COMMENT '亮点介绍',
  `interval_name` varchar(255) DEFAULT NULL COMMENT '区间名称',
  `create_by` varchar(50) DEFAULT NULL COMMENT '创建人',
  `create_time` datetime DEFAULT NULL COMMENT '创建时间',
  `update_by` varchar(50) DEFAULT NULL COMMENT '更新人',
  `update_time` datetime DEFAULT NULL COMMENT '更新时间',
  PRIMARY KEY (`id`),
  KEY `idx_product_name` (`product_name`),
  KEY `idx_spu_name` (`spu_name`),
  KEY `idx_ticket_kind_name` (`ticket_kind_name`),
  KEY `idx_category_id` (`category_id`)
